import numpy as np
import matplotlib.pyplot as plt
from scipy.integrate import odeint
from scipy.signal import find_peaks
from sklearn.linear_model import LinearRegression
from sklearn.preprocessing import PolynomialFeatures

# ============================================================================
# 1. DOPAMINE KINETICS IN THE BRAIN
# ============================================================================
def dopamine_kinetics(D, t, k_synth, k_deg):
    """Simple dopamine turnover model"""
    dD_dt = k_synth - k_deg * D
    return dD_dt

k_synth_healthy = 1.0
k_synth_parkinsons = 0.3
k_deg = 0.5

D0 = 0
t_dopamine = np.linspace(0, 10, 200)

D_healthy = odeint(dopamine_kinetics, D0, t_dopamine, args=(k_synth_healthy, k_deg))
D_parkinsons = odeint(dopamine_kinetics, D0, t_dopamine, args=(k_synth_parkinsons, k_deg))

D_ss_healthy = k_synth_healthy / k_deg
D_ss_parkinsons = k_synth_parkinsons / k_deg

# ============================================================================
# 2. LEVODOPA PHARMACOKINETICS (with & without COMT inhibitors)
# ============================================================================

t_pharma = np.linspace(0, 12, 300)

# L-DOPA alone: fast elimination
t_half_ldopa = 1.0
k_elim_ldopa = np.log(2) / t_half_ldopa
C0_ldopa = 10.0
C_ldopa_alone = C0_ldopa * np.exp(-k_elim_ldopa * t_pharma)

# With COMT inhibitor: slower elimination
t_half_with_comt = 2.5
k_elim_with_comt = np.log(2) / t_half_with_comt
C_ldopa_with_comt = C0_ldopa * np.exp(-k_elim_with_comt * t_pharma)

# Calculate oscillation metric: peak-to-trough variance
def calculate_oscillation(C):
    """Measure oscillation intensity (0 = smooth, 1 = highly pulsatile)"""
    peaks, _ = find_peaks(C, height=np.max(C) * 0.3)
    if len(peaks) < 2:
        return 0
    peak_heights = C[peaks]
    oscillation = np.std(peak_heights) / np.mean(peak_heights) if np.mean(peak_heights) > 0 else 0
    return oscillation

# ============================================================================
# 3. CONTINUOUS INFUSION PUMP vs PULSATILE DOSING
# ============================================================================

t_infusion = np.linspace(0, 24, 500)

# Pulsatile: 4 doses of L-DOPA
dosing_times_pulsatile = np.array([0, 6, 12, 18])
C_pulsatile = np.zeros_like(t_infusion)
for dose_time in dosing_times_pulsatile:
    dose_amplitude = 10.0
    dose_width = 0.8
    C_pulsatile += dose_amplitude * np.exp(-((t_infusion - dose_time) ** 2) / (2 * dose_width ** 2))

# Continuous infusion: constant delivery
C_continuous_infusion = np.ones_like(t_infusion) * 6.0 + 0.5 * np.random.normal(0, 1, len(t_infusion))
C_continuous_infusion = np.convolve(C_continuous_infusion, np.ones(10)/10, mode='same')  # Smooth

# ============================================================================
# 4. RECEPTOR DESENSITIZATION & STORAGE CAPACITY LOSS
# ============================================================================
# Model: Available dopamine storage decreases over time
# Receptor sensitivity also decreases (downregulation)

years_therapy = np.linspace(0, 35, 100)

# Storage capacity decline
storage_capacity = 100 * np.exp(-0.08 * years_therapy)

# Receptor sensitivity decline (desensitization)
receptor_sensitivity = 100 * np.exp(-0.05 * years_therapy)

# Effective dopamine response = actual dopamine * storage * sensitivity
effective_response = 100 * np.exp(-0.08 * years_therapy) * np.exp(-0.05 * years_therapy)

# With adjunct therapy (MAO-B inhibitors): slower decline
receptor_sensitivity_with_mao = 100 * np.exp(-0.02 * years_therapy)
effective_response_with_mao = 100 * np.exp(-0.08 * years_therapy) * np.exp(-0.02 * years_therapy)

# ============================================================================
# 5. PULSATILE DOPAMINE SIMULATION (declining response over time)
# ============================================================================

t_detailed = np.linspace(0, 26, 500)
dopamine_pulsatile_early = np.zeros_like(t_detailed)
dopamine_pulsatile_late = np.zeros_like(t_detailed)

dosing_times = np.array([0, 4, 8, 12, 16, 20, 24])

# Early therapy: good response
for i, dose_time in enumerate(dosing_times):
    dose_amplitude = 8 * (0.90 ** i)
    dose_width = 1.5
    dopamine_pulsatile_early += dose_amplitude * np.exp(-((t_detailed - dose_time) ** 2) / (2 * dose_width ** 2))

# Late therapy: poor response
for i, dose_time in enumerate(dosing_times):
    dose_amplitude = 3 * (0.75 ** i)  # Lower amplitude, faster decay
    dose_width = 1.2
    dopamine_pulsatile_late += dose_amplitude * np.exp(-((t_detailed - dose_time) ** 2) / (2 * dose_width ** 2))

# ============================================================================
# 6. MINI ML MODEL: Predicting Symptom Scores from Treatment Variables
# ============================================================================

# Synthetic training data: years on therapy, dosage (mg), age, symptom score
np.random.seed(42)
n_samples = 50

years_on_therapy = np.random.uniform(1, 30, n_samples)
dosage_mg = 200 + years_on_therapy * 100 + np.random.normal(0, 50, n_samples)  # Dosage increases over time
age = 50 + years_on_therapy * 0.3 + np.random.normal(0, 5, n_samples)

# Symptom score: depends on years (disease progression) and dosage (treatment effect)
# Higher score = worse symptoms
symptom_score = (
    30 +                                          # baseline
    years_on_therapy * 1.5 +                      # disease progression
    0.01 * dosage_mg +                            # medication burden
    0.3 * age +                                   # age effect
    np.random.normal(0, 5, n_samples)             # noise
)
symptom_score = np.clip(symptom_score, 0, 100)

# Features and target
X = np.column_stack([years_on_therapy, dosage_mg, age])
y = symptom_score

# Polynomial regression (degree 2) for non-linear relationship
poly = PolynomialFeatures(degree=2)
X_poly = poly.fit_transform(X)
model = LinearRegression()
model.fit(X_poly, y)

# Predictions for visualization
years_range = np.linspace(1, 30, 50)
dosage_range = 200 + years_range * 100
age_range = 50 + years_range * 0.3
X_pred = np.column_stack([years_range, dosage_range, age_range])
X_pred_poly = poly.transform(X_pred)
y_pred = model.predict(X_pred_poly)

# Model R² score
from sklearn.metrics import r2_score
y_train_pred = model.predict(X_poly)
r2 = r2_score(y, y_train_pred)

