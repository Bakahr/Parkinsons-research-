ax1b.plot(t_pharma, C_ldopa_with_comt, 'purple', linewidth=2.5, label=f'+ COMT inhibitor (t₁/₂ = {t_half_with_comt}h)')
ax1b.fill_between(t_pharma, C_ldopa_alone, alpha=0.15, color='blue')
ax1b.fill_between(t_pharma, C_ldopa_with_comt, alpha=0.15, color='purple')
ax1b.axhline(C0_ldopa / 2, color='gray', linestyle=':', alpha=0.5)
ax1b.set_xlabel('Time (hours)', fontsize=10)
ax1b.set_ylabel('[L-DOPA]', fontsize=10)
ax1b.set_title('COMT Inhibitors: Extended Half-Life', fontsize=11, fontweight='bold')
ax1b.legend(fontsize=9)
ax1b.grid(True, alpha=0.3)

# Plot 1c: Oscillation Reduction
ax1c = fig.add_subplot(gs[0, 2])
osc_alone = calculate_oscillation(C_ldopa_alone)
osc_with_comt = calculate_oscillation(C_ldopa_with_comt)
treatments = ['L-DOPA\nAlone', 'L-DOPA +\nCOMT Inhibitor']
oscillations = [osc_alone, osc_with_comt]
colors_osc = ['blue', 'purple']
bars = ax1c.bar(treatments, oscillations, color=colors_osc, alpha=0.7, edgecolor='black', linewidth=1.5)
ax1c.set_ylabel('Oscillation Index', fontsize=10)
ax1c.set_title('Reduced Plasma Oscillations', fontsize=11, fontweight='bold')
ax1c.set_ylim([0, max(oscillations) * 1.3])
for bar, osc in zip(bars, oscillations):
    height = bar.get_height()
    ax1c.text(bar.get_x() + bar.get_width()/2., height,
              f'{osc:.3f}', ha='center', va='bottom', fontsize=10, fontweight='bold')
ax1c.grid(True, alpha=0.3, axis='y')

# ========== Row 2: Continuous Infusion & Receptor Desensitization ==========

# Plot 2a: Pulsatile vs Continuous Infusion
ax2a = fig.add_subplot(gs[1, 0:2])
ax2a.plot(t_infusion, C_pulsatile, 'r-', linewidth=2, label='Pulsatile (4 doses/day)', alpha=0.8)
ax2a.plot(t_infusion, C_continuous_infusion, 'g-', linewidth=2.5, label='Continuous Infusion Pump')
ax2a.fill_between(t_infusion, C_pulsatile, alpha=0.15, color='red')
